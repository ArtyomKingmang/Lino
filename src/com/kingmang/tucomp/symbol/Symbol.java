package com.kingmang.tucomp.symbol;

public class Symbol {

    private String type;
    private String value;

    public Symbol(){}

    public String getValue() {
        return value;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setValue(String value) {
        this.value = value;
    }

}
